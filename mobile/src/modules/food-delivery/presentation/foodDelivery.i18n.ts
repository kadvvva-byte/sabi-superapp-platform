const dictionaries: Record<FoodDeliveryLocale, FoodDeliveryDict> = { ru, en, uz, zh };

export function resolveFoodDeliveryLocale(language?: string | null): FoodDeliveryLocale {
  const normalized = String(language || "ru").toLowerCase();
  if (normalized.startsWith("zh")) return "zh";
  if (normalized.startsWith("uz")) return "uz";
  if (normalized.startsWith("en")) return "en";
  return "ru";
}

export function tFoodDelivery(key: string, locale: FoodDeliveryLocale = "ru") {
  return dictionaries[locale]?.[key] ?? dictionaries.ru[key] ?? dictionaries.en[key] ?? key;
}

export function foodDeliveryApprovalLabel(status: FoodDeliveryApprovalStatus, locale: FoodDeliveryLocale) {
  return tFoodDelivery(`status.${status}`, locale);
}

export function foodDeliveryPaymentLabel(status: FoodDeliveryPaymentStatus, locale: FoodDeliveryLocale) {
  return tFoodDelivery(`payment.${status}`, locale);
}

export function foodDeliveryKitchenLabel(status: FoodDeliveryKitchenStatus, locale: FoodDeliveryLocale) {
  return tFoodDelivery(`kitchen.${status}`, locale);
}
