export function findSabiQrFunction(code: string | null | undefined): SabiQrFunctionDefinition | null {
  if (!code) return null;
  return SABI_QR_FUNCTION_CATALOG.find((item) => item.code === code) ?? null;
}

export function assertSabiQrFunction(code: SabiQrFunctionCode): SabiQrFunctionDefinition {
  const found = findSabiQrFunction(code);
  if (!found) {
    throw new Error(`Unknown Sabi QR function: ${code}`);
  }
  return found;
}

export function getSabiQrFunctionsBySurface(surface: SabiQrFunctionDefinition["surface"]): SabiQrFunctionDefinition[] {
  return SABI_QR_FUNCTION_CATALOG.filter((item) => item.surface === surface);
}
