function resolveLocale(): WholesaleMarketLocale {
  const language = getAppLanguage();
  if (language === "uz" || language === "zh" || language === "en") return language;
  return "ru";
}

export function useWholesaleMarketText() {
  const locale = resolveLocale();
  const fallback = TEXT.ru;
  const active = TEXT[locale] ?? fallback;

  return {
    locale,
    t(key: string) {
      return active[key] ?? fallback[key] ?? key;
    },
  };
}
