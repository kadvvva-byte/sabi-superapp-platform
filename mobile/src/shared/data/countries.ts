export function getCountryByCode(code: string): CountryItem | undefined {
  return COUNTRIES.find((item) => item.code === code.toUpperCase());
}

export function getCountryLanguage(code: string): SupportedLanguageCode {
  return getCountryByCode(code)?.languageCode ?? getDefaultLanguageByCountry(code);
}

export function getCountriesByLanguage(
  languageCode: SupportedLanguageCode
): CountryItem[] {
  return COUNTRIES.filter((item) => item.supportedLanguageCodes.includes(languageCode));
}

