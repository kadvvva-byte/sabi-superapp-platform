function normalizeLanguageCode(language?: string | null): string {
  return String(language || "")
    .trim()
    .replace(/_/g, "-")
    .toLowerCase();
}

function languageKey(language?: string | null): string {
  const normalized = normalizeLanguageCode(language);
  if (normalized === "fa-af") return "fa-AF";
  return normalized.split("-")[0] || "en";
}

function appSectionKey(key: string): string | null {
  if (key.startsWith("profile.appSection.")) {
    return key.replace("profile.appSection.", "profile.app.");
  }
  if (key.startsWith("profile.premiumSection.premiumCard.")) {
    return key.replace("profile.premiumSection.premiumCard.", "profile.premiumSection.premium.");
  }
  if (key.startsWith("profile.walletSection.walletCard.")) {
    return key.replace("profile.walletSection.walletCard.", "profile.wallet.wallet.");
  }
  if (key.startsWith("profile.walletSection.settings.")) {
    return key.replace("profile.walletSection.settings.", "profile.wallet.settings.");
  }
  return null;
}

export function getProfileMainTranslationOverride(
  language: string | null | undefined,
  key: string,
): string | undefined {
  const lang = languageKey(language);
  const table = PROFILE_TRANSLATIONS[lang] || PROFILE_TRANSLATIONS.en;
  const direct = table[key];
  if (typeof direct === "string") return direct;

  const mappedKey = appSectionKey(key);
  if (mappedKey && typeof table[mappedKey] === "string") {
    return table[mappedKey];
  }

  return undefined;
}
