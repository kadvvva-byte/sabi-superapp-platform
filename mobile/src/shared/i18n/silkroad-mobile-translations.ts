export type SilkRoadLocale =
  | "en"
  | "ru"
  | "uz"
  | "zh"
  | "tr"
  | "kk"
  | "ky"
  | "tg";

export type SilkRoadCurrency = "COIN" | "SELLER_DEFINED";

type Dict = Record<string, string>;
type LocaleDict = Record<SilkRoadLocale, Dict>;

const FALLBACK_LOCALE: SilkRoadLocale = "en";

export function resolveSilkRoadLocale(language?: string | null): SilkRoadLocale {
  const normalized = String(language || "").trim().replace(/_/g, "-").toLowerCase();
  if (normalized.startsWith("ru")) return "ru";
  if (normalized.startsWith("uz")) return "uz";
  if (normalized.startsWith("zh")) return "zh";
  if (normalized.startsWith("tr")) return "tr";
  if (normalized.startsWith("kk")) return "kk";
  if (normalized.startsWith("ky")) return "ky";
  if (normalized.startsWith("tg")) return "tg";
  return "en";
}

export function getSilkRoadDisplayName(language?: string | null): string {
  const locale = resolveSilkRoadLocale(language);
  switch (locale) {
    case "ru":
      return "Шёлковый путь";
    case "uz":
      return "Ipak yo‘li";
    case "zh":
      return "丝绸之路";
    case "tr":
      return "İpek Yolu";
    case "kk":
      return "Жібек жолы";
    case "ky":
      return "Жибек жолу";
    case "tg":
      return "Роҳи абрешим";
    default:
      return "SilkRoad";
  }
}

export function localizeSilkRoadMiniAppItem<T extends { id?: string; kind?: string; title?: string; subtitle?: string }>(
  item: T,
  language?: string | null,
): T {
  if (item?.id !== "marketplace" && item?.kind !== "marketplace") return item;
  const locale = resolveSilkRoadLocale(language);
  return {
    ...item,
    title: getSilkRoadDisplayName(language),
    subtitle: tSilkRoad("mini.subtitle", locale),
  } as T;
}

export function localizeSilkRoadMiniAppItems<T extends { id?: string; kind?: string; title?: string; subtitle?: string }>(
  items: T[],
  language?: string | null,
): T[] {
  return items.map((item) => localizeSilkRoadMiniAppItem(item, language));
}

export function formatSilkRoadPrice(amount: number, currency: SilkRoadCurrency, language?: string | null): string {
  const locale = resolveSilkRoadLocale(language);
  if (currency === "SELLER_DEFINED") {
    return tSilkRoad("price.sellerDefined", locale);
  }

  const safeAmount = Number.isFinite(amount) ? amount : 0;
  const number = new Intl.NumberFormat(resolveIntlLocale(locale), {
    maximumFractionDigits: 0,
  }).format(safeAmount);

  const prefix = tSilkRoad("price.from", locale);
  return `${prefix} ${number} COIN`;
}

export function tSilkRoad(key: string, language?: string | null): string {
  const locale = resolveSilkRoadLocale(language);
  return SILKROAD_TRANSLATIONS[locale]?.[key] ?? SILKROAD_TRANSLATIONS[FALLBACK_LOCALE][key] ?? key;
}

function resolveIntlLocale(locale: SilkRoadLocale): string {
  switch (locale) {
    case "ru":
      return "ru-RU";
    case "uz":
      return "uz-Latn-UZ";
    case "zh":
      return "zh-CN";
    case "tr":
      return "tr-TR";
    case "kk":
      return "kk-KZ";
    case "ky":
      return "ky-KG";
    case "tg":
      return "tg-TJ";
    default:
      return "en-US";
  }
}

